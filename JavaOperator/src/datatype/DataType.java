public class DataType {
	public static void main(String[] args) {
		// 데이터 타입 + 변수 형태
			byte y; // 정수형, 8bit
			short s; // 정수형, 16bit
			int a; // 정수형, 32bit
			long l; // 정수형, 64bit
			
			float f; // 실수형 
			double d; // 실수형, 무한대의 개념.. 소숫점이 있는데 측정 불가?
			
			char c; // 알파벳-문자형
			boolean b; // true or false
			
			int a2;
			a2 = 6; // means integer type variable인 a2에 숫자 5를 할당
			
			int a3 = 5;			
			System.out.println(a3);
						
			// line 28 + 29 = 30?
			
	} 
}
